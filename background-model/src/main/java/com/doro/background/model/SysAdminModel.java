package com.doro.background.model;

public class SysAdminModel {
	private int adminId;
	private String adminName;
	private String adminPwd;
	private String confirmAdminPwd;
	
	public int getAdminId() {
		return adminId;
	}

	public void setAdminId(int adminId) {
		this.adminId = adminId;
	}

	public String getAdminName() {
		return adminName;
	}

	public void setAdminName(String adminName) {
		this.adminName = adminName;
	}

	public String getAdminPwd() {
		return adminPwd;
	}

	public void setAdminPwd(String adminPwd) {
		this.adminPwd = adminPwd;
	}

	public String getConfirmAdminPwd() {
		return confirmAdminPwd;
	}

	public void setConfirmAdminPwd(String confirmAdminPwd) {
		this.confirmAdminPwd = confirmAdminPwd;
	}
	
	
}
